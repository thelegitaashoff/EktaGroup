class ProviderModel {
  final String id;
  final String name;
  final String profession;
  final String location;
  final String avatarUrl;
  final bool online;
  final String phone;

  ProviderModel({
    required this.id,
    required this.name,
    required this.profession,
    required this.location,
    required this.avatarUrl,
    required this.online,
    required this.phone,
  });
}
